Consigne :

Créez un formulaire HTML qui permet à l'utilisateur de saisir son nom et son prénom. Lorsque l'utilisateur soumet le formulaire, affichez un message de bienvenue personnalisé qui utilise les valeurs saisies.

Utilisez les variables GET ou POST pour récupérer les données du formulaire et assurez-vous que les valeurs sont correctement validées. 