Consigne : Écrire un programme PHP qui demande à l'utilisateur d'entrer une note entre 0 et 20 et qui affiche la mention correspondante (A, B, C, D ou E) en fonction de la note. Les mentions sont définies selon le barème suivant :

A : note >= 16
B : 14 <= note < 16
C : 12 <= note < 14
D : 10 <= note < 12
E : note < 10