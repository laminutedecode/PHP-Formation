<!-- Une fonction sans argument est une fonction qui ne prend aucun paramètre lorsqu'elle est appelée. Voici un exemple de déclaration et d'utilisation d'une fonction sans argument en PHP : -->


<?php
// Déclaration d'une fonction sans argument
function afficherMessage() {
    echo "Bonjour !";
}

// Appel de la fonction
afficherMessage(); // affiche "Bonjour !"
?>



<!-- Dans cet exemple, nous avons défini une fonction nommée afficherMessage qui ne prend pas de paramètre. Dans la fonction, nous avons simplement utilisé l'instruction echo pour afficher le message "Bonjour !".

Ensuite, nous avons appelé la fonction afficherMessage en utilisant son nom suivi de parenthèses, sans aucun argument entre les parenthèses. Lorsque la fonction est appelée, elle exécute le code qu'elle contient et affiche "Bonjour !" à l'utilisateur.

Les fonctions sans argument sont souvent utilisées pour effectuer des tâches simples et répétitives qui ne nécessitent pas de données en entrée. -->