<?php
// Initialisation de la variable i à 1
$i = 1;

// Boucle while qui affiche les nombres impairs de 1 à 20
while ($i <= 20) {
    echo $i . "<br>";
    $i += 2;
}

// Explications :

// Tout d'abord, nous initialisons la variable $i à 1.
// Ensuite, nous utilisons la boucle while pour parcourir les nombres de 1 à 20.
// Dans la boucle, nous affichons la valeur de $i à chaque itération, qui est un nombre impair.
// Enfin, nous incrémentons la valeur de $i de 2 à chaque itération pour passer au nombre impair suivant.
?>
