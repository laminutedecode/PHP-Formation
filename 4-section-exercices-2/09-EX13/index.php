Écrire un programme PHP qui demande à l'utilisateur de saisir son âge.
Utiliser l'opérateur ternaire pour vérifier si l'utilisateur a 18 ans ou plus.
Si c'est le cas, afficher le message "Vous êtes majeur".
Sinon, afficher le message "Vous êtes mineur".
Afficher également l'âge de l'utilisateur en utilisant la fonction echo.