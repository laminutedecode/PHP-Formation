Considérez le scénario suivant :

Vous avez créé un site web qui permet à l'utilisateur de choisir une langue d'affichage pour le site. Vous voulez stocker la langue choisie par l'utilisateur dans un cookie, afin de lui permettre de retrouver cette langue lorsqu'il revient sur le site ultérieurement.

La consigne :

Écrivez un script PHP qui affiche une page d'accueil avec un formulaire permettant à l'utilisateur de choisir une langue d'affichage. Lorsque le formulaire est soumis, la langue choisie par l'utilisateur est stockée dans un cookie et l'utilisateur est redirigé vers la même page d'accueil, qui doit maintenant être affichée dans la langue choisie.