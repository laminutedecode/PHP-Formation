Consigne:

Créez une classe en PHP nommée "Voiture". Cette classe devrait avoir les propriétés suivantes :

Marque (privée)
Modèle (privée)
Année (publique)
Couleur (publique)
Vitesse actuelle (publique, valeur par défaut : 0)
Elle devrait également avoir les méthodes suivantes :

Un constructeur qui prend les arguments marque et modèle et les affecte aux propriétés correspondantes.
Une méthode "afficher" qui affiche les informations sur la voiture sous la forme "Marque - Modèle (Année) - Couleur - Vitesse : Vitesse_actuelle km/h".
Une méthode "accelerer" qui prend en argument une valeur de vitesse et l'ajoute à la vitesse actuelle.