Consigne :
Créez une classe Statistiques qui permet de calculer diverses statistiques sur un tableau de nombres. La classe doit avoir les méthodes suivantes :

calculerMoyenne() : calcule la moyenne des nombres dans le tableau.
calculerVariance() : calcule la variance des nombres dans le tableau.
calculerEcartType() : calcule l'écart-type des nombres dans le tableau.
calculerMediane() : calcule la médiane des nombres dans le tableau.
Utilisez cette classe pour calculer ces statistiques sur un tableau de nombres donné.