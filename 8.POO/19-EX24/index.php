Consigne :
Créez une classe Forme qui représente une forme géométrique quelconque. Cette classe doit avoir les méthodes suivantes :

calculerAire() : calcule l'aire de la forme.
calculerPerimetre() : calcule le périmètre de la forme.
Créez ensuite deux sous-classes de Forme : Rectangle et Cercle. La classe Rectangle doit avoir les propriétés longueur et largeur, tandis que la classe Cercle doit avoir la propriété rayon.

Utilisez ces classes pour calculer l'aire et le périmètre d'un rectangle de dimensions 5x10 et d'un cercle de rayon 3.